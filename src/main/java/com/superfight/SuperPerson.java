package com.superfight;

public class SuperPerson {

  private int id;
  private String name;
  private int health;
  private int strength;
  private int armour_rating;
  private String special_move;
  private int wins;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getHealth() {
    return health;
  }

  public void setHealth(int health) {
    this.health = health;
  }

  public int getStrength() {
    return strength;
  }

  public void setStrength(int strength) {
    this.strength = strength;
  }

  public int getArmour_rating() {
    return armour_rating;
  }

  public void setArmour_rating(int armour_rating) {
    this.armour_rating = armour_rating;
  }

  public String getSpecial_move() {
    return special_move;
  }

  public void setSpecial_move(String special_move) {
    this.special_move = special_move;
  }

  public int getWins() {
    return wins;
  }

  public void setWins(int wins) {
    this.wins = wins;
  }
}
